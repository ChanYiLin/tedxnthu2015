        <h1 style="letter-spacing:0px;font-weight:normal">TEDxNTHU</h1>
        <p>
        TED是一個致力於「分享ideas」的非營利組織，通常以18分鐘內強而有力的短講呈現。
        TED組織設立「TEDx」計畫，幫助社群、個人或組織能夠舉辦各地大會，分享與連結ideas。
        TEDxNTHU則透過TED官方授權，於2013年成立並加入TEDx行列，期望讓更多可貴的ideas能被匯聚、
        看見、分享，更期望觀眾未來運用所得創造價值、回饋社會。
        </p>
        <br>
        <br>
        <h1>主題介紹</h1>
        <p>
        TEDxNTHU2015年會以「實踐」為主軸，「初‧練」為主題，展開一場匯聚動能的盛大聚會。「初‧練」── 初念於心、勤練不輟、起身實踐。為實現理想實踐中途難免踉蹌，
        但我們相信理想之深而不忘初衷的堅持，會支持不斷練習的毅力，終能實踐藍圖、成就更好的自己、為社會留下美好軌跡。
        </p>
